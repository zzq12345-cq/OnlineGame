#include "iostream"
using namespace std;
/*
 * 问题描述
编写递归函数将输入的整数逐个数字输出。如输入12345，输出1 2 3 4 5，每个数字之间用空格隔开。

输入描述
一个正整数n，表示n组案例。
每组案例由一个正整数m构成。
输出描述
针对每组案例，将输入的整数逐个数字输出，每个数字之间用空格隔开，最后一位数字后面没有空格。
每组案例输出完都要换行。
样例输入
1
12345
样例输出
1 2 3 4 5
 */
void f(int m){
    // 基础情况：如果m是一个单位数(m < 10)直接输出
    if (m < 10) {
        cout << m;
    } else {
        // 递归情况：先递归输出m / 10的结果，然后输出m % 10
        f(m / 10);
        cout << " " << m % 10;
    }
}

int main()
{
    int n;
    cin >> n;
    while (n--){
        int m;
        cin >> m;
        f(m);
        cout << endl;
    }
    return 0;
}